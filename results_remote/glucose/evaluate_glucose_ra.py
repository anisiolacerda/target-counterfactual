"""
S5.2: RA Evaluation on Glucose-Insulin Simulator

Applies reach-avoid constrained selection to counterfactual candidates
generated by the glucose simulator. Uses oracle (simulator) trajectories
for safety evaluation.

Metrics:
- Top-1: fraction of patients where selected plan has lowest true loss
- In-Target: terminal glucose in [70, 180] mg/dL
- Safety: all glucose values in [50, 250] mg/dL
- Feasibility: fraction of candidates satisfying both constraints
"""

import pickle
import numpy as np
import os
import argparse

# Thresholds
TARGET_LO = 70.0
TARGET_HI = 180.0
SAFETY_LO = 50.0
SAFETY_HI = 250.0
TARGET_MID = (TARGET_LO + TARGET_HI) / 2  # 125 mg/dL


def compute_true_loss(glucose_traj):
    """Compute true loss = squared distance of terminal glucose from target midpoint."""
    terminal = glucose_traj[:, :, -1]  # (N, k) — last time step for all candidates
    return (terminal - TARGET_MID) ** 2


def evaluate_ra(cf_data, model_losses=None, tau=6):
    """Evaluate RA-constrained selection on glucose counterfactual data.

    Args:
        cf_data: dict with 'all_sequences' (N,k,tau), 'true_glucose_trajectories' (N,k,tau)
        model_losses: (N,k) ELBO losses from VCIP. If None, uses true loss as proxy.
        tau: prediction horizon

    Returns:
        results dict with per-patient metrics
    """
    seqs = cf_data['all_sequences']
    true_traj = cf_data['true_glucose_trajectories']
    N, k, _ = true_traj.shape

    # True losses
    true_losses = compute_true_loss(true_traj)  # (N, k)

    # If no model losses provided, use true loss (oracle ranking)
    if model_losses is None:
        model_losses = true_losses

    results = {
        'elbo_top1': [], 'elbo_safe': [], 'elbo_intgt': [], 'elbo_tl': [],
        'ra_top1': [], 'ra_safe': [], 'ra_intgt': [], 'ra_tl': [],
        'feasibility': [],
    }

    for i in range(N):
        ml = model_losses[i]  # (k,)
        tl = true_losses[i]   # (k,)
        traj = true_traj[i]   # (k, tau)

        # Oracle feasibility check
        terminal_bg = traj[:, -1]
        max_bg = traj.max(axis=1)
        min_bg = traj.min(axis=1)

        in_target = (terminal_bg >= TARGET_LO) & (terminal_bg <= TARGET_HI)
        safe = (max_bg <= SAFETY_HI) & (min_bg >= SAFETY_LO)
        feasible = in_target & safe

        feas_rate = feasible.sum() / k
        results['feasibility'].append(feas_rate)

        # Best true loss (ground truth optimum)
        best_true_idx = np.argmin(tl)

        # ELBO selection
        elbo_idx = np.argmin(ml)
        results['elbo_top1'].append(1.0 if elbo_idx == best_true_idx else 0.0)
        results['elbo_safe'].append(1.0 if safe[elbo_idx] else 0.0)
        results['elbo_intgt'].append(1.0 if in_target[elbo_idx] else 0.0)
        results['elbo_tl'].append(tl[elbo_idx])

        # RA-constrained selection
        if feasible.any():
            ra_ml = ml.copy()
            ra_ml[~feasible] = np.inf
            ra_idx = np.argmin(ra_ml)
            results['ra_top1'].append(1.0 if ra_idx == best_true_idx else 0.0)
            results['ra_safe'].append(1.0 if safe[ra_idx] else 0.0)
            results['ra_intgt'].append(1.0 if in_target[ra_idx] else 0.0)
            results['ra_tl'].append(tl[ra_idx])
        else:
            # Fallback to ELBO
            results['ra_top1'].append(results['elbo_top1'][-1])
            results['ra_safe'].append(results['elbo_safe'][-1])
            results['ra_intgt'].append(results['elbo_intgt'][-1])
            results['ra_tl'].append(results['elbo_tl'][-1])

    return {k: np.array(v) for k, v in results.items()}


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--data_dir', type=str, required=True)
    parser.add_argument('--model_dir', type=str, default=None, help='VCIP model dir (if None, uses oracle ranking)')
    parser.add_argument('--gamma', type=int, default=4)
    parser.add_argument('--output_dir', type=str, default='glucose_results')
    args = parser.parse_args()

    os.makedirs(args.output_dir, exist_ok=True)

    print("=" * 60)
    print("Glucose RA Evaluation (gamma=%d)" % args.gamma)
    print("=" * 60)

    # Evaluate at multiple tau values
    taus = [2, 4, 6, 8]

    print("\n%-5s | %-8s | %-18s | %-18s | %-10s" % (
        "tau", "Feas%", "ELBO (Safe/InTgt)", "RA (Safe/InTgt)", "RA InTgt+"))
    print("-" * 70)

    all_results = {}

    for tau in taus:
        cf_path = os.path.join(args.data_dir, 'gamma_%d' % args.gamma, 'test_cf_tau%d.pkl' % tau)
        if not os.path.exists(cf_path):
            print("  tau=%d: CF data not found, skipping" % tau)
            continue

        with open(cf_path, 'rb') as f:
            cf_data = pickle.load(f)

        # TODO: Load VCIP model losses if model_dir is provided
        # For now, use oracle ranking (true losses) to establish upper bound
        results = evaluate_ra(cf_data, model_losses=None, tau=tau)

        all_results[tau] = results

        feas = 100 * results['feasibility'].mean()
        e_safe = 100 * results['elbo_safe'].mean()
        e_intgt = 100 * results['elbo_intgt'].mean()
        r_safe = 100 * results['ra_safe'].mean()
        r_intgt = 100 * results['ra_intgt'].mean()
        improvement = r_intgt - e_intgt

        print("%-5d | %6.1f%% | %6.1f%% / %6.1f%% | %6.1f%% / %6.1f%% | %+6.1f pp" % (
            tau, feas, e_safe, e_intgt, r_safe, r_intgt, improvement))

    # Detailed summary
    print("\n" + "=" * 60)
    print("DETAILED RESULTS")
    print("=" * 60)

    for tau in taus:
        if tau not in all_results:
            continue
        r = all_results[tau]
        print("\ntau=%d (%d patients):" % (tau, len(r['feasibility'])))
        print("  Feasibility: %.1f%% (per-patient: [%.0f%%, %.0f%%])" % (
            100 * r['feasibility'].mean(), 100 * r['feasibility'].min(), 100 * r['feasibility'].max()))
        print("  ELBO:  Top-1=%.1f%%  Safe=%.1f%%  InTgt=%.1f%%  AvgTL=%.4f" % (
            100 * r['elbo_top1'].mean(), 100 * r['elbo_safe'].mean(),
            100 * r['elbo_intgt'].mean(), r['elbo_tl'].mean()))
        print("  RA:    Top-1=%.1f%%  Safe=%.1f%%  InTgt=%.1f%%  AvgTL=%.4f" % (
            100 * r['ra_top1'].mean(), 100 * r['ra_safe'].mean(),
            100 * r['ra_intgt'].mean(), r['ra_tl'].mean()))

    # Save results
    save_path = os.path.join(args.output_dir, 'glucose_ra_gamma%d.pkl' % args.gamma)
    with open(save_path, 'wb') as f:
        pickle.dump(all_results, f)
    print("\nResults saved to %s" % save_path)


if __name__ == '__main__':
    main()
